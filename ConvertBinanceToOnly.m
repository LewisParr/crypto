function transactions = ConvertBinanceToOnly(binance)
%CONVERTBINANCETOONLY Summary of this function goes here

noneAsset = CreateCategoricalNone(height(binance));
nanValue = NaN(size(noneAsset));
zeroValue = zeros(size(noneAsset));

transactions = table( ...
    binance.UTC_Time, ...
    binance.Coin, ...
    binance.Change, ...
    nanValue, ...
    nanValue, ...
    noneAsset, ...
    zeroValue, ...
    zeroValue, ...
    zeroValue, ...
    noneAsset, ...
    zeroValue, ...
    zeroValue, ...
    zeroValue);

transactions = RenameTransactionColumns(transactions);

return
end

